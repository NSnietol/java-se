    /**
     * @param colaTipo ,true para cola de servicio y false para cola de espera
     * @return el número dónde se va a ubicar a la persona pero devuelve -1 sino
     * hay espacio
     */
    public int disponibilidad(boolean colaTipo) {

        if (colaTipo == true) {
            for (int i = 0; i < colaServicioIconos.size(); i++) {
                // Los JLabel que no estén visibles son los que están disponibles
                if (colaServicioIconos.get(i).getIcon() == null) {

                    return i;

                }
            }

        } else {
            for (int i = 0; i < colaEsperaIconos.size(); i++) {
                // Los JLabel que no estén visibles son los que están disponibles
                if (colaEsperaIconos.get(i).getIcon() == null) {

                    return i;

                }

            }

        }

        return -1;
    }

    public boolean sacarDeCola(boolean colaTipo) {

        ImageIcon moverCola;
        ImageIcon sacandoServicio;

        if (colaTipo) {

            if (!colaServicioIconos.isEmpty()) {
                sacandoServicio = (ImageIcon) colaEsperaIconos.get(0).getIcon();

                for (int i = 0; i < colaServicioIconos.size() - 1; i++) {
                    colaServicioIconos.get(i).setIcon(colaServicioIconos.get(i + 1).getIcon());

                }
                colaServicioIconos.get(colaServicioIconos.size() - 1).setIcon(null);

            }

        } else if (!colaEsperaIconos.isEmpty()) {
            moverCola = (ImageIcon) colaEsperaIconos.get(0).getIcon();

            for (int i = 0; i < colaEsperaIconos.size() - 1; i++) {
                colaEsperaIconos.get(i).setIcon(colaEsperaIconos.get(i + 1).getIcon());

            }
            colaEsperaIconos.get(colaEsperaIconos.size() - 1).setIcon(null);

            int disponibilidad = disponibilidad(true);
            if (disponibilidad != -1) {
                colaServicioIconos.get(disponibilidad).setIcon(moverCola);
            } else {
                insertarPersona.suspend();
                try {
                    Thread.sleep(2000);
                } catch (InterruptedException ex) {
                    Logger.getLogger(vistaCafeteria2.class.getName()).log(Level.SEVERE, null, ex);
                }
                insertarPersona.resume();
            }

        }

        return true;

    }


    Thread mesas = new Thread() {
        @Override
        public void run() {

            Random generador = new Random();
            while (animancionON) {
                try {
                    int puesto = generador.nextInt(15);

                    if (puesto >= 0 && puesto <= 14) {

                        Boolean estadoMesa = ocupacionMesas.get(puesto);

                        // True si tiene gente
                        if (estadoMesa) {

                            mesasLabel.get(puesto).setIcon(new javax.swing.ImageIcon(getClass().getResource("/pictures/MesaT1.png")));

                        } else {
                            mesasLabel.get(puesto).setIcon(new javax.swing.ImageIcon(getClass().getResource("/pictures/MesaT2.png")));

                        }
                        ocupacionMesas.set(puesto, !estadoMesa);

                    }

                    Thread.sleep(1000 * generador.nextInt(15));
                } catch (InterruptedException ex) {
                    Logger.getLogger(vistaCafeteria2.class.getName()).log(Level.SEVERE, null, ex);
                }

            }

        }

    };

    Thread insertarPersona = new Thread() {
        @Override
        public void run() {



            boolean bandera = true;
            int tamano = listaClienteCorridaActual.getClientes().size();

            while (animancionON && contPersonas < tamano) {

                try {

                    Cliente clientico = listaClienteCorridaActual.getClientes().get(contPersonas++);

                    hora += clientico.getTiempoLlegada();

                    establecerHora();

                    cantidad(contPersonas);

                    Thread.sleep(factorAceleracion * clientico.getTiempoLlegada());
                    System.out.println("Velocidad entrada " + (factorAceleracion * clientico.getTiempoLlegada()));
                    switch (clientico.getTipo()) {
                        case "joven": {
                            InsertarColaIcon(clientico.getGenero().equals("Hombre"), 0);
                            break;
                        }
                        case "Adulto": {
                            InsertarColaIcon(clientico.getGenero().equals("Hombre"), 1);
                            break;
                        }
                        case "Adulto mayor": {
                            InsertarColaIcon(clientico.getGenero().equals("Hombre"), 2);
                            break;
                        }

                    }

                    if (jLabelPersonaServicio19.getIcon() != null) {

                        Thread.sleep(1000 * 2);
                    }

                } catch (InterruptedException ex) {
                    Logger.getLogger(vistaCafeteria2.class.getName()).log(Level.SEVERE, null, ex);
                }

                if (bandera) {
                    hiloSacarCola.start();
                    bandera = false;
                }
            }

        }

    };

